#include "libft.h"

size_t		ft_wrdl(char const *s, int c)
{
	int wordlen;

	wordlen = 0;
	while (*s == c)
		s++;
	while (*s != c && *s != '\0')
	{
		wordlen++;
		s++;
	}
	return (wordlen);
}
